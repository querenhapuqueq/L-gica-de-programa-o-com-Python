 # strings

'''1 - len -- saber o tamanho de uma string'''

a = 'João'
print(len(a))

'''Concatenando uma string - Juntando uma string'''

nome = 'Querén'
sobrenome = 'Hapuque'
resultado = nome + '' + sobrenome
print(resultado)

'''Fatiamento Simples - Substrings das Strings - Podemos acessar caracteres específicos de uma String através de conotações [].
O primeiro caractere de uma String sempre começará no índice 0.'''

caractere = 'Eu sou uma gostosa'
print(caractere[0])
print(caractere[5])
print(caractere[12])

p = 'python'
print('tamanho: ',len(p))
print(p[:5])
print(p[2:6])
print(p[0:7])
print(p[:])
print(p[-3:-1])
print(p[-3:])




